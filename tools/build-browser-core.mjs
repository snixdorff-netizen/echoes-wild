#!/usr/bin/env node
/**
 * Generates tools/echoes-core.browser.js from tools/echoes-core.mjs (single source of truth).
 */
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  HABITATS,
  PERSONAS,
  FACING_BONUS_THRESHOLD,
  angleDiff,
  scoreAnimalTarget,
  clipQualityFromScore,
  selectRecordingTarget,
  facingBonusFromDiff,
  nearestActiveCaller,
  qualityLabel,
  integrityGain,
  integrityLoss,
  applyIdentification,
  shouldCompleteExpedition,
  markHabitatDone,
  personaHint,
} from './echoes-core.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const out = join(__dirname, 'echoes-core.browser.js');

function fnSource(fn) {
  return fn.toString().replace(/^(\s*)export\s+async\s+function/, '$1function').replace(/^(\s*)export\s+function/, '$1function');
}

const body = `/* eslint-disable */
/* AUTO-GENERATED by tools/build-browser-core.mjs — do not edit by hand */
(function (global) {
  const HABITATS = ${JSON.stringify(HABITATS)};
  const PERSONAS = ${JSON.stringify(PERSONAS)};
  const FACING_BONUS_THRESHOLD = ${FACING_BONUS_THRESHOLD};

  ${fnSource(angleDiff)}
  ${fnSource(scoreAnimalTarget)}
  ${fnSource(clipQualityFromScore)}
  ${fnSource(selectRecordingTarget)}
  ${fnSource(facingBonusFromDiff)}
  ${fnSource(nearestActiveCaller)}
  ${fnSource(qualityLabel)}
  ${fnSource(integrityGain)}
  ${fnSource(integrityLoss)}
  ${fnSource(applyIdentification)}
  ${fnSource(shouldCompleteExpedition)}
  ${fnSource(markHabitatDone)}
  ${fnSource(personaHint)}

  global.EchoesCore = {
    HABITATS: HABITATS,
    PERSONAS: PERSONAS,
    FACING_BONUS_THRESHOLD: FACING_BONUS_THRESHOLD,
    angleDiff: angleDiff,
    scoreAnimalTarget: scoreAnimalTarget,
    clipQualityFromScore: clipQualityFromScore,
    selectRecordingTarget: selectRecordingTarget,
    facingBonusFromDiff: facingBonusFromDiff,
    nearestActiveCaller: nearestActiveCaller,
    qualityLabel: qualityLabel,
    integrityGain: integrityGain,
    integrityLoss: integrityLoss,
    applyIdentification: applyIdentification,
    shouldCompleteExpedition: shouldCompleteExpedition,
    markHabitatDone: markHabitatDone,
    personaHint: personaHint,
  };
})(typeof window !== 'undefined' ? window : globalThis);
`;

writeFileSync(out, body);
console.log('Wrote', out);